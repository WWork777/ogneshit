// Функция для получения направления по slug
export const directions = Object.values(directionsData);

export function getDirectionBySlug(slug) {
  return directions.find((direction) => direction.slug === slug);
}

// Серверная функция для поиска направления по slug
export async function getDirectionBySlugServer(slug) {
  return new Promise((resolve) => {
    const direction = directions.find((direction) => direction.slug === slug);
    resolve(direction);
  });
}

// Функция для поиска направления по ID
export function getDirectionById(id) {
  return directionsData[id];
}

// Получить все направления
export function getAllDirections() {
  return directions;
}
