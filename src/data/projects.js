export const projects = Object.values(projectsData);
// Получить проект по slug
export function getProjectBySlug(slug) {
  return projects.find((project) => project.slug === slug);
}

export async function getProjectBySlugServer(slug) {
  return new Promise((resolve) => {
    const project = projects.find((project) => project.slug === slug);
    resolve(project);
  });
}

export function getProjectById(id) {
  return projectsData[id];
}

// Получить список всех проектов (краткие данные)
export function getAllProjects() {
  return Object.values(projectsData).map((project) => ({
    id: project.id,
    title: project.title,
    city: project.city,
    country: project.country,
    slug: project.slug,
    image: project.image,
  }));
}

// Получить список проектов для лендинга
export function getProjectsForLanding() {
  // Возвращаем все проекты без дублирования
  return getAllProjects();
}
